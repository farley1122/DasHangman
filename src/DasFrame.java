    private void btnGuessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuessActionPerformed
        if (lives == 0) {
            lblErrors.setText("Game Over! Press restart to play again");
            System.out.println("YOU LOSE!");
        } else {

            boolean correctGuess = false;

            if (txtGuess.getText().length() != 1) {
                System.out.println("All is not well shipmate");
            } else {

                String guess = txtGuess.getText().toUpperCase();
                char letter = guess.charAt(0);
                char[] wordLetters = word.toCharArray();

                if (letGuess.contains(txtGuess.getText())) {
                } else {
                    letGuess += txtGuess.getText() + " ";
                }
                lblGuessed1.setText(letGuess);
                for (int x = 0; x < wordLetters.length; x++) {
                    if (letter == wordLetters[x]) {
                        changeLetter(x, letter);
                        correctGuess = true;
                    }
                }
                if (correctGuess == false) {
                    lives--;
                    lblPic.setIcon(new ImageIcon("../src/man" + lives + ".jpg"));

                }
            }
        }
        String lblsTogether = lbl0.getText()+lbl1.getText()+lbl2.getText()+lbl3.getText()+lbl4.getText();
        if (lblsTogether.contains(word)) {
            lblErrors.setText("You win!");
        }        
        txtGuess.setText("");


    }//GEN-LAST:event_btnGuessActionPerformed

