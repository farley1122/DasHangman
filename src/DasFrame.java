    private void changeLetter(int x, char letter) {
        if (x == 0) {
            lbl0.setText("" + letter);
        } else if (x == 1) {
            lbl1.setText("" + letter);
        } else if (x == 2) {
            lbl2.setText("" + letter);
        } else if (x == 3) {
            lbl3.setText("" + letter);
        } else if (x == 4) {
            lbl4.setText("" + letter);
        }
    }

    public void chooseWord() {
        while (wordBak.equals(word)) {

            int count = (int) (1 + Math.random() * 5);
            switch (count) {
                case 1:
                    word = "WORLD";
                    break;
                case 2:
                    word = "MAGIC";
                    break;
                case 3:
                    word = "STAIR";
                    break;
                case 4:
                    word = "BEARS";
                    break;
                case 5:
                    word = "THERE";
                    break;
            }
        }
        System.out.println("Your word is " + word);
        wordBak = word;
    }

